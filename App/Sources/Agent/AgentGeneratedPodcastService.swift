import Foundation
import os.log

// MARK: - AgentGeneratedPodcastService
//
// Manages the "Agent Generated" virtual podcast subscription and its on-disk
// episode audio files.
//
// The subscription uses the sentinel feed URL `agent-generated://podcast` so it
// is excluded from OPML export, feed-refresh scheduling, and the download
// service's auto-download evaluator.  Callers branch on
// `PodcastSubscription.isAgentGenerated` rather than comparing URL strings.
//
// Audio files live under:
//   Application Support / podcastr / agent-episodes / <episodeID>.m4a
//
// Each call to `publishEpisode` creates a new `Episode` with
// `downloadState = .downloaded` and a `file://` `enclosureURL` pointing at
// the stitched m4a.  The player reads local URLs exactly like remote ones
// through the existing AudioEngine local-file fallback.

struct AgentGeneratedPodcastService: Sendable {

    private static let logger = Logger.app("AgentGeneratedPodcastService")

    // MARK: - Sentinel

    /// The sentinel feed URL that identifies the agent-generated virtual
    /// podcast subscription. Never used for HTTP requests.
    static let sentinelFeedURL = URL(string: "agent-generated://podcast")!

    // MARK: - Storage

    /// Root directory for agent-generated episode audio files.
    static func audioDirectory() throws -> URL {
        let support = try FileManager.default.url(
            for: .applicationSupportDirectory,
            in: .userDomainMask,
            appropriateFor: nil,
            create: true
        )
        let dir = support
            .appendingPathComponent("podcastr", isDirectory: true)
            .appendingPathComponent("agent-episodes", isDirectory: true)
        try FileManager.default.createDirectory(at: dir, withIntermediateDirectories: true)
        return dir
    }

    /// Returns the canonical m4a URL for an agent episode by its UUID.
    /// The file need not exist yet — callers write here before calling
    /// `publishEpisode`.
    static func audioFileURL(episodeID: UUID) throws -> URL {
        try audioDirectory().appendingPathComponent("\(episodeID.uuidString).m4a")
    }

    // MARK: - Subscription management

    /// Returns the existing agent-generated subscription ID, or creates the
    /// subscription and returns its new ID.  Safe to call repeatedly — the
    /// subscription is only inserted once (keyed on `sentinelFeedURL`).
    @MainActor
    static func ensureSubscription(in store: AppStateStore) -> UUID {
        if let existing = store.subscription(feedURL: sentinelFeedURL) {
            return existing.id
        }
        let sub = PodcastSubscription(
            feedURL: sentinelFeedURL,
            title: "Agent Generated",
            author: "Podcast Agent",
            imageURL: nil,
            description: "Episodes generated by your AI agent.",
            autoDownload: AutoDownloadPolicy(mode: .off),
            notificationsEnabled: false,
            isAgentGenerated: true
        )
        store.addSubscription(sub)
        logger.info("Created agent-generated subscription \(sub.id, privacy: .public)")
        return sub.id
    }

    // MARK: - Episode publishing

    /// Registers a finished m4a file as an episode on the agent-generated
    /// podcast and inserts it into `AppStateStore`. The file at `audioURL`
    /// must already exist on disk.
    ///
    /// - Parameters:
    ///   - title: Episode title displayed in the library.
    ///   - description: Plain-text episode description.
    ///   - audioURL: `file://` URL of the stitched m4a.
    ///   - durationSeconds: Computed duration; supply when known to populate
    ///     the progress scrubber immediately.
    ///   - store: The app's single-source-of-truth store.
    /// - Returns: The newly-inserted `Episode`.
    @MainActor
    @discardableResult
    static func publishEpisode(
        title: String,
        description: String,
        audioURL: URL,
        durationSeconds: TimeInterval?,
        in store: AppStateStore
    ) -> Episode {
        let subscriptionID = ensureSubscription(in: store)
        let episodeID = UUID()
        let episode = Episode(
            id: episodeID,
            subscriptionID: subscriptionID,
            guid: episodeID.uuidString,
            title: title,
            description: description,
            pubDate: Date(),
            duration: durationSeconds,
            enclosureURL: audioURL,
            enclosureMimeType: "audio/mp4",
            downloadState: .downloaded(
                localFileURL: audioURL,
                byteCount: (try? FileManager.default.attributesOfItem(atPath: audioURL.path)[.size] as? Int64) ?? 0
            )
        )
        store.upsertEpisodes([episode], forSubscription: subscriptionID, evaluateAutoDownload: false)
        logger.info("Published agent episode '\(title, privacy: .public)' id=\(episodeID, privacy: .public)")
        return episode
    }
}
